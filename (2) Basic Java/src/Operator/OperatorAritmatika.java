package Operator;

public class OperatorAritmatika {

	public static void main(String[] args) {
		System.out.println("Operasi penjumlahan");
		int hasilPenjumlahan = 5 + 1;
		System.out.println("Hasil dari 5 + 1 adalah "+hasilPenjumlahan);
		System.out.println();
		
		System.out.println("Operasi pengurangan");
		int hasilPengurangan = 4 - 1;
		System.out.println("Hasil dari 4 -1 adalah "+hasilPengurangan);
		System.out.println();
		
		System.out.println("Operasi perkalian");
		int hasilPerkalian = 5 * 5;
		System.out.println("Hasil dari 5 * 5 adalah "+hasilPerkalian);
		System.out.println();
		
		System.out.println("Operasi pembagian");
		int hasilPembagian = 20 / 2;
		System.out.println("Hasil dari 20 / 2 adalah "+hasilPembagian);
		System.out.println();
		
		System.out.println("Operasi habis bagi");
		int hasilSisa = 8 % 2;
		System.out.println("Hasil dari 8 % 2 adalah "+hasilSisa);
		System.out.println();
		int hasilSisa2 = 9 % 2;
		System.out.println("Hasil dari 9 % 2 adalah "+hasilSisa2);
		System.out.println();
		
		

	}

}
